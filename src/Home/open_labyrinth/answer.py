D = {"S": (1, 0), "N": (-1, 0), "W": (0, -1), "E": (0, 1)}


def checkio(maze_map):
    reached = {(1, 1): ""}  # (pos) => route
    queue = [(1, 1)]
    while len(queue) > 0:
        pos = queue.pop()
        route = reached[pos]
        for dstr, diff in D.items():
            new_pos = (pos[0]+diff[0], pos[1]+diff[1])
            if new_pos == (10, 10):
                return route + dstr
            if maze_map[new_pos[0]][new_pos[1]] == 0 and new_pos not in reached:
                queue.insert(0, new_pos)
                reached[new_pos] = route + dstr


